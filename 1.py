"Книга коштує a гривень і b копійок. Визначте, скільки гривень і копійок потрібно заплатити за n книг. Значення вводяться користувачем у порядку a, b, n на окремих рядках, а сума до сплати в одному рядку через пропуск: кількість гривень і копійок відповідно. Виконав Петюх О.В."
# Введення значень a, b, n
a = int(input("Введіть кількість гривень: "))
b = int(input("Введіть кількість копійок: "))
n = int(input("Введіть кількість книг: "))

# Обчислення суми за n книг
total_amount = n * (a + b / 100)

# Виведення результату
print("Сума до сплати: {:.2f} гривень".format(total_amount))
